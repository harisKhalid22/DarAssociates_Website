      <p>For residential work, smaller projects and renovations the most common method is hourly. For medium to large size non-residential projects the most common method is a percentage of construction cost</p>
      <h3>Hourly Fees</h3>
        <p>Typical Architectural hourly fees range from $125 to $250 per hour. This method is mostly used for small projects, residential projects or for services where the time to complete the work is not known.</p>
      <h3>Cost per Square Foot</h3>
        <p>The cost per square foot method is less commonly used. It does not work well with remodeling projects and is usually only used in the design of custom homes. It assesses a fee based on the square foot measure of the largest concept presented to the Client. Generally, this method limits revisions to a specific number, generally three, and if the Client desires further revisions, the Architect will bill on an hourly basis. Fees range from $2 to $5 per square foot for concept development and $1 to $4 per square foot for construction document production.</p>
      <h3>Percentage of Construction Cost</h3>
        <p>Fees based on a percentage of construction cost is most commonly used in medium to large commercial projects. The average fees for a project of typical complexity is 10% -15% for renovations and 8%-12% for new construction.</p>
      <h3>Fixed Fees</h3>
        <p>Fixed Fees is most commonly used in projects where the scope can be clearly defined. Sometimes a percentage fee is converted to a fixed fee after the schematic design phase is complete and a cost estimate has been prepared. The fixed fee is usually calculated by estimating the hours required to complete the work, or based on the expected result of the percentage fee.</p>
      <h3>Combination</h3>
        <p>Combination is most commonly used when portions of the work are billed hourly where the time required to complete the work is not known, and the remainder of the fee is a fixed fee or a percentage of construction based fee.</p>
